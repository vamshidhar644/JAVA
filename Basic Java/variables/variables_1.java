package variables;

public class variables_1 {
	public static void main(String args[]) {
		int __int_abc = 5; // 4 bytes => 32 bits => -2,147,483,648 to 2,147,483,648
		
		float __float_abc = 3.333f; // use f at the end
		
		double __double_abc = 4.5; 
		
		System.out.println("int: "+__int_abc);
		System.out.println("float: "+__float_abc);
		System.out.println("double: "+__double_abc);
		
	}
}
